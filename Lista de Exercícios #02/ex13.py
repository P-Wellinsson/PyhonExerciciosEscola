data = int(input('Digite um número para dizer o dia da semana, ex: 1 - Domingo, 2 - Segunda, etc : '))
if data == 1:
    print('Domingo')
elif data == 2:
    print('Segunda')
elif data == 3:
    print('Terça')
elif data == 4:
    print('Quarta')
elif data == 5:
    print('Quinta')
elif data == 6:
    print('Sexta')
elif data == 7:
    print('Sabado')
else:
    print('Valor inválido')
